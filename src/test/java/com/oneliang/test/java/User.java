package com.oneliang.test.java;

import java.util.Date;

public class User {
	private String idTemp;
	private String nameTemp;
	private String passwordTemp;
	private Date loginTime;
	/**
	 * @return the idTemp
	 */
	public String getIdTemp() {
		return idTemp;
	}
	/**
	 * @param idTemp the idTemp to set
	 */
	public void setIdTemp(String idTemp) {
		this.idTemp = idTemp;
	}
	/**
	 * @return the nameTemp
	 */
	public String getNameTemp() {
		return nameTemp;
	}
	/**
	 * @param nameTemp the nameTemp to set
	 */
	public void setNameTemp(String nameTemp) {
		this.nameTemp = nameTemp;
	}
	/**
	 * @return the passwordTemp
	 */
	public String getPasswordTemp() {
		return passwordTemp;
	}
	/**
	 * @param passwordTemp the passwordTemp to set
	 */
	public void setPasswordTemp(String passwordTemp) {
		this.passwordTemp = passwordTemp;
	}
	/**
	 * @return the loginTime
	 */
	public Date getLoginTime() {
		return loginTime;
	}
	/**
	 * @param loginTime the loginTime to set
	 */
	public void setLoginTime(Date loginTime) {
		this.loginTime = loginTime;
	}
}
